// CheeseGames i18n - Motor de traducción limpio
// ============================================

const defaultLanguage = 'en';

function getSavedLanguage() {
    try {
        return localStorage.getItem('cheesegames-language') || defaultLanguage;
    } catch (err) {
        return defaultLanguage;
    }
}

function saveLanguage(language) {
    try {
        localStorage.setItem('cheesegames-language', language);
    } catch (err) { /* ignore */ }
}

function getLanguageFromURL() {
    try {
        const params = new URLSearchParams(window.location.search);
        const lang = params.get('lang');
        if (lang && translations[lang]) return lang;
    } catch (err) { /* ignore */ }
    return null;
}

function updateUrlLanguage(language) {
    try {
        const url = new URL(window.location.href);
        url.searchParams.set('lang', language);
        history.replaceState(null, '', url.pathname + '?' + url.searchParams.toString() + (url.hash || ''));
    } catch (err) { /* ignore */ }
}

function translatePage(language) {
    const strings = translations[language] || translations.en;
    document.documentElement.lang = language;

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const text = strings[key] || translations.en[key];
        if (!text) return;
        const tag = el.tagName.toLowerCase();
        if (tag === 'input' || tag === 'textarea') {
            el.placeholder = text;
        } else if (tag === 'meta') {
            el.setAttribute('content', text);
        } else {
            el.textContent = text;
        }
    });

    if (strings['page.title']) document.title = strings['page.title'];
    const metaDesc = document.querySelector('meta[name="description"]');
    if (metaDesc && strings['page.description']) metaDesc.setAttribute('content', strings['page.description']);

    updateUrlLanguage(language);
}

function applyLangFlags(activeLang) {
    document.querySelectorAll('.lang-flag').forEach(btn => {
        const isActive = btn.dataset.lang === activeLang;
        btn.classList.toggle('active', isActive);
        btn.setAttribute('aria-pressed', isActive ? 'true' : 'false');
    });
}

function initI18n() {
    const lang = getLanguageFromURL() || getSavedLanguage() || 'en';
    translatePage(lang);
    applyLangFlags(lang);

    document.querySelectorAll('.lang-flag').forEach(btn => {
        btn.addEventListener('click', function () {
            const selected = this.dataset.lang;
            saveLanguage(selected);
            translatePage(selected);
            applyLangFlags(selected);
        });
    });
}

// Ejecutar al cargar — funciona tanto si el DOM ya está listo como si no
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initI18n);
} else {
    initI18n();
}

window.translatePage = translatePage;
